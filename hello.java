public class hello {
    public static void main(String args[]) {

        int a = 1;
        String b = "Hello World";
        System.out.println(a);
        System.out.println(b);
    }
}

// public 은 누구나 메소드에 접근할 수 있다는 의미
// 메소드에 Static 가 지정되어 있을때 이 메소드는 인스턴스 생성없이 실행 할 수 있다는 의미
// void는 리턴값이 없다는 의미
// String은 문자열을 나타내는 자바의 자료형
// args 는 여러개의 값으로 이루어진 배열임을 뜻한다.
// System.out.println 는 메소드로 들어오는 무자열 값을 화면에 출력한다.

// 정수 int, long / int 를 주로 사용한다.
// 실수 float,double , 디폴트가 double 
